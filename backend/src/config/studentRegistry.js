const normalizeName = (v = '') => v.trim().replace(/\s+/g, ' ').toUpperCase();
export const normalizeUsn = (v = '') => {
  const s = v.trim().replace(/\s+/g, '').toUpperCase();
  // Accept both 1MJ24IS... and 1MVJ24IS... formats
  return s.replace(/^1MVJ/, '1MJ');
};

export const getApprovedStudentByUsn = (usn) => {
  const nUsn = normalizeUsn(usn);
  return approvedStudents.find((s) => normalizeUsn(s.usn) === nUsn) || null;
};

// Flexible name matching - allows minor spacing/case differences
export const getApprovedStudent = ({ usn, name }) => {
  const match = getApprovedStudentByUsn(usn);
  if (!match) return null;
  // If name is provided, do a loose match (ignore extra spaces, case)
  if (name) {
    const nName = normalizeName(name);
    const mName = normalizeName(match.name);
    // Allow if names match exactly, or submitted name is contained in official name, or vice versa
    if (nName !== mName && !mName.includes(nName) && !nName.includes(mName)) return null;
  }
  return match;
};

